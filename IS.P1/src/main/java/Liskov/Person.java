package Liskov;

public class Person {
    
    private String name;
    private String LastName;

    public String getName() {
        return name;
    }

    public String getLastName() {
        return LastName;
    }

    public Person(String name, String LastName) {
        this.name = name;
        this.LastName = LastName;
    }
    
            
            
  
    
}
